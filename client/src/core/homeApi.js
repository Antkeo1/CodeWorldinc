export const uri = "http://localhost:8000";

export const create = (userId, token, post) => {
  return fetch(`${uri}/post/new`, {
    method: "POST",
    headers: {
      Accept: "application/json",
      Authorization: `Bearer ${token}`,
    },
    body: post,
  })
    .then((response) => {
      return response.json();
    })
    .catch((err) => console.log(err));
};

